package ita.social.utils;

public class AdviceException extends Exception {
	private static final long serialVersionUID = 1L;

	public AdviceException() {
		super();
	}

	public AdviceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AdviceException(String arg0) {
		super(arg0);
	}

	public AdviceException(Throwable arg0) {
		super(arg0);
	}
}
	
	


